import {mapState} from "vuex";
import Pagination from "../components/Pagination";
import {contentWayPoint} from "../utils/helpers"
export default {
    name: "home",
    components: {Pagination, carousel },
    data() {
        return {
            currentPage: 1
        };
    },
    computed: {
        ...mapState({
            postsTop: state => state.posts.listTop,
            postTop: state => state.posts.postTop,
            posts: state => state.posts.posts,
            paginate: state => state.posts.paginate
        })
    },
    created() {
        this.$store.dispatch("posts/getPostsTop");
        console.log(21)
        $('.content-main').owlCarousel({
            animateOut: 'fadeOut',
            animateIn: 'fadeIn',
            autoplay: true,
            loop: true,
            items:4,
            margin: 0,
            stagePadding: 0,
            nav: true,
            dots: true,
            navText: ['<span class="ion-ios-arrow-back">', '<span class="ion-ios-arrow-forward">'],
            responsive:{
                0:{
                    items: 4
                },
                600:{
                    items: 4
                },
                1000:{
                    items: 4
                }
            }
        });
    },

    mounted() {
        this.waypoint();
        this.currentPage = parseInt(this.$route.query.page) || 1
        this.$store.dispatch("posts/getPosts", {page : this.currentPage});
        // console.log(this.postTop);
        // console.log(this.postsTop);
    },
    methods: {
        waypoint() {
            $(window).stellar({
                responsive: true,
                parallaxBackgrounds: true,
                parallaxElements: true,
                horizontalScrolling: false,
                hideDistantElements: false,
                scrollProperty: 'scroll'
            });

            $('.js-fullheight').css('height', $(window).height());
            $(window).resize(function () {
                $('.js-fullheight').css('height', $(window).height());
            });
            var i = 0;
            $('.ftco-animate').waypoint(function (direction) {

                if (direction === 'down' && !$(this.element).hasClass('ftco-animated')) {

                    i++;

                    $(this.element).addClass('item-animate');
                    setTimeout(function () {

                        $('body .ftco-animate.item-animate').each(function (k) {
                            var el = $(this);
                            setTimeout(function () {
                                var effect = el.data('animate-effect');
                                if (effect === 'fadeIn') {
                                    el.addClass('fadeIn ftco-animated');
                                } else if (effect === 'fadeInLeft') {
                                    el.addClass('fadeInLeft ftco-animated');
                                } else if (effect === 'faadeInRight') {
                                    el.addClass('fadeInRight ftco-animated');
                                } else {
                                    el.addClass('fadeInUp ftco-animated');
                                }
                                el.removeClass('item-animate');
                            }, k * 50, 'easeInOutExpo');
                        });

                    }, 100);

                }

            }, {offset: '95%'});

            $('.content-main').owlCarousel({
                animateOut: 'fadeOut',
                animateIn: 'fadeIn',
                autoplay: true,
                loop: true,
                items:4,
                margin: 0,
                stagePadding: 0,
                nav: true,
                dots: true,
                navText: ['<span class="ion-ios-arrow-back">', '<span class="ion-ios-arrow-forward">'],
                responsive:{
                    0:{
                        items: 4
                    },
                    600:{
                        items: 4
                    },
                    1000:{
                        items: 4
                    }
                }
            });
        },
        getSlugID(post) {
            return post.slug + "--" + post.id;
        },
        handleChangePage(page) {
            if (page <= 0) {
                page = 1;
            } else if (page >= this.paginate.last_page) {
                page = this.paginate.last_page;
            }

            this.currentPage = page;
            this.$router.push({path: "/", query: {page: page}});
            this.$store.dispatch("posts/getPosts", {page: page});
        }
    }
};
</script>
